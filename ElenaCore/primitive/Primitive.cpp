	const std::shared_ptr<CVertexBuffer>& CPrimitive::createPlane()
	{
		if (m_pPlane != nullptr) return m_pPlane;
		std::vector<float> Vertices{
			// positions        normals            texture coords
			 0.5f, 0.0f,  0.5f, 0.0f, 1.0f, 0.0f,  1.0f, 1.0f, // top right
			 0.5f, 0.0f, -0.5f, 0.0f, 1.0f, 0.0f,  1.0f, 0.0f, // bottom right
			-0.5f, 0.0f, -0.5f, 0.0f, 1.0f, 0.0f,  0.0f, 0.0f, // bottom left
			-0.5f, 0.0f,  0.5f, 0.0f, 1.0f, 0.0f,  0.0f, 1.0f, // top left 
		};
		std::vector<unsigned int> Indices{
			0, 1, 3, // first Triangle
			1, 2, 3, // second Triangle
		};
		m_pPlane = std::make_shared<CVertexBuffer>(Vertices, Indices, std::vector<int>{3, 3, 2});
		return m_pPlane;
	}

	const std::shared_ptr<CVertexBuffer>& CPrimitive::createQuad()
	{
		if (m_pQuad != nullptr) return m_pQuad;
		std::vector<float> Vertices {
			// positions   // texCoords
			-1.0f,  1.0f,  0.0f, 1.0f,
			-1.0f, -1.0f,  0.0f, 0.0f,
			 1.0f, -1.0f,  1.0f, 0.0f,
			 1.0f,  1.0f,  1.0f, 1.0f
		};
		std::vector<unsigned int> Indices{
			0, 1, 2, // first Triangle
			0, 2, 3, // second Triangle
		};
		m_pQuad = std::make_shared<CVertexBuffer>(Vertices, Indices, std::vector<int>{2, 2});
		return m_pQuad;
	}
