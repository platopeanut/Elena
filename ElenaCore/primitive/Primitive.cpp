	std::shared_ptr<CVertexBuffer> CPrimitive::m_pCube{ nullptr };
	std::shared_ptr<CVertexBuffer> CPrimitive::m_pPlane{ nullptr };
	std::shared_ptr<CVertexBuffer> CPrimitive::m_pQuad{ nullptr };

	void CPrimitive::clear()
	{
		m_pCube = nullptr;
		m_pPlane = nullptr;
		m_pQuad = nullptr;
	}

